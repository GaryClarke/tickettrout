            <h2>Ticket Trout Profile - Explained</h2>
            <p>The profile page is not the show-stopping feature of Ticket Trout but it is one the most important when it comes to achieving our main objective; <b>putting tickets in the hands of the people who genuinely want to go and see the show.</b></p>

            <h3>How Do We Achieve This?</h3>

                <p>Our intelligent points system helps to identify the behaviour of touts and that of genuine fans. There is no single step which will identify an individual as a tout but with automated tracking of user behaviour we can build the picture.</p>

            <h3>How Are The Points Awarded?</h3>
            <p>In short, points are awarded for every action which would indicate that the customer is a genuine concert goer. A few examples:</p>
            <ul>
                <li>A fully completed profile</li>
                <li>Registering in advance of events</li>
                <li>Payment details which cross reference accurately against the profile information.</li>
                <li>Not always taking the full allocation of tickets</li>
                <li>Having <i>sensible</i> wishlists which reflect those of genuine fans</li>
            </ul>
            <h3>Is The System Tout Proof?</h3>
            <p>Yes and no. We can't keep touts from using the site but they'll be wasting their time if they think that they are going to make money from it. They'd be better off waiting tables.</p>
